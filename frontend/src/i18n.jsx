export function getLangName(uiCode, langCode) {
  const map = LANG_NAMES[uiCode] || LANG_NAMES.en;
  const key = String(langCode || "").toLowerCase();
  return map[key] || (LANG_NAMES.en[key] || langCode);
}

const LangContext = createContext({ code: "en", t: (k) => k });

export function LangProvider({ code, children }) {
  const t = (key, vars) => translate(code, key, vars);
  return (
    <LangContext.Provider value={{ code, t }}>{children}</LangContext.Provider>
  );
}

export function useT() {
  return useContext(LangContext);
}
