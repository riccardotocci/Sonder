const LS_KEY = "sonder_ui_lang";

export function getInitialLang() {
  try {
    const stored = localStorage.getItem(LS_KEY);
    if (stored && STRINGS[stored]) return stored;
  } catch {
    /* ignore */
  }
  const nav = (navigator.language || "en").slice(0, 2).toLowerCase();
  return STRINGS[nav] ? nav : "en";
}

export function persistLang(code) {
  try {
    localStorage.setItem(LS_KEY, code);
  } catch {
    /* ignore */
  }
}

export function getLang(code) {
  return UI_LANGUAGES.find((l) => l.code === code) || UI_LANGUAGES[0];
}

function interpolate(str, vars) {
  if (!vars) return str;
  return str.replace(/\{(\w+)\}/g, (m, k) =>
    vars[k] !== undefined ? String(vars[k]) : m
  );
}

export function translate(code, key, vars) {
  const dict = STRINGS[code] || STRINGS.en;
  const val = dict[key] !== undefined ? dict[key] : STRINGS.en[key];
  if (val === undefined) return key;
  return interpolate(val, vars);
}

export function getStudioStrings(code) {
  const base = STRINGS.en.studio;
  const loc = (STRINGS[code] || STRINGS.en).studio || {};
  return { ...base, ...loc };
}

export function getThinkingSteps(code) {
  const dict = STRINGS[code] || STRINGS.en;
  const steps = dict.thinkingSteps || STRINGS.en.thinkingSteps;
  return Array.isArray(steps) ? steps : [];
}

